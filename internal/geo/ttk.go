// InsideTTKRing reports whether (lat, lon) falls inside the ТТК polygon.
func InsideTTKRing(lat, lon float64) bool {
	return ttkRing.inside(lat, lon)
}

// DistanceToTTKRingKM returns the shortest distance in kilometers from
// (lat, lon) to the ТТК boundary.
func DistanceToTTKRingKM(lat, lon float64) float64 {
	return ttkRing.distanceKM(lat, lon)
}
